    glClear(GL_COLOR_BUFFER_BIT)
    glColor3f(0,0,0)
    glBegin(GL_POINTS)
    for i in range(0, 400, 1):
        glVertex2f(edge_list[i][1], edge_list[i][0])
    glEnd()
    glFlush()

